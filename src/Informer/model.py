import torch
import torch.nn as nn
import torch.nn.functional as F
from src.Informer.embed import DataEmbedding
from src.Informer.attn import FullAttention, ProbAttention, AttentionLayer
from src.Informer.encoder import Encoder, EncoderLayer, ConvLayer, EncoderStack
from src.Informer.decoder import Decoder, DecoderLayer
from pytorch_model_summary import summary

class Informer(nn.Module):
    def __init__(self, enc_in, dec_in, c_out, seq_len, label_len, out_len, 
                factor=5, d_model=512, n_heads=8, e_layers=3, d_layers=2, d_ff=512, 
                dropout=0.0, attn='prob', embed='fixed', freq='h', activation='gelu', 
                output_attention = False, distil=True, mix=True, t_input_dim : int = 4
                ):
        super(Informer, self).__init__()
        self.enc_in = enc_in
        self.dec_in = dec_in
        self.seq_len = seq_len
        self.pred_len = out_len
        self.attn = attn
        self.output_attention = output_attention
        self.t_input_dim  = t_input_dim
        
        self.input_dim = enc_in + t_input_dim

        # Encoding
        self.enc_embedding = DataEmbedding(enc_in, d_model, embed, freq, dropout)
        self.dec_embedding = DataEmbedding(dec_in, d_model, embed, freq, dropout)
        # Attention
        Attn = ProbAttention if attn=='prob' else FullAttention
        # Encoder
        self.encoder = Encoder(
            [
                EncoderLayer(
                    AttentionLayer(Attn(False, factor, attention_dropout=dropout, output_attention=output_attention), 
                                d_model, n_heads, mix=False),
                    d_model,
                    d_ff,
                    dropout=dropout,
                    activation=activation
                ) for l in range(e_layers)
            ],
            [
                ConvLayer(
                    d_model
                ) for l in range(e_layers-1)
            ] if distil else None,
            norm_layer=torch.nn.LayerNorm(d_model)
        )
        # Decoder
        self.decoder = Decoder(
            [
                DecoderLayer(
                    AttentionLayer(Attn(True, factor, attention_dropout=dropout, output_attention=False), 
                                d_model, n_heads, mix=mix),
                    AttentionLayer(FullAttention(False, factor, attention_dropout=dropout, output_attention=False), 
                                d_model, n_heads, mix=False),
                    d_model,
                    d_ff,
                    dropout=dropout,
                    activation=activation,
                )
                for l in range(d_layers)
            ],
            norm_layer=torch.nn.LayerNorm(d_model)
        )
        # self.end_conv1 = nn.Conv1d(in_channels=label_len+out_len, out_channels=out_len, kernel_size=1, bias=True)
        # self.end_conv2 = nn.Conv1d(in_channels=d_model, out_channels=c_out, kernel_size=1, bias=True)
        self.projection = nn.Linear(d_model, c_out, bias=True)
        
    def forward(self, x : torch.Tensor, x_target : torch.Tensor, enc_self_mask=None, dec_self_mask=None, dec_enc_mask=None):
        
        x_mark_enc = x[:,:,0:self.t_input_dim]
        x_enc = x[:,:,self.t_input_dim:]
        
        x_mark_dec = x_target[:,:,0:self.t_input_dim]
        x_dec = x_target[:,:,self.t_input_dim:]
        
        enc_out = self.enc_embedding(x_enc, x_mark_enc)
        enc_out, attns = self.encoder(enc_out, attn_mask=enc_self_mask)

        dec_out = self.dec_embedding(x_dec, x_mark_dec)
        dec_out = self.decoder(dec_out, enc_out, x_mask=dec_self_mask, cross_mask=dec_enc_mask)
        dec_out = self.projection(dec_out)
        
        # dec_out = self.end_conv1(dec_out)
        # dec_out = self.end_conv2(dec_out.transpose(2,1)).transpose(1,2)
        if self.output_attention:
            return dec_out[:,-self.pred_len:,:], attns
        else:
            return dec_out[:,-self.pred_len:,:] # [B, L, D]
        
    def summary(self):
        sample_data = torch.zeros((1, self.seq_len, self.enc_in + self.t_input_dim))
        sample_target = torch.zeros((1, self.seq_len, self.enc_in + self.t_input_dim))
        
        return summary(self, sample_data, sample_target, None, None, None, batch_size = 1, show_input = True, show_hierarchical=False,print_summary=True)

    def predict(self, x : torch.Tensor, target_len : int):
        
        output = torch.zeros((self.seq_len + target_len, self.input_dim,)).to(x.device)
        output[0:self.seq_len,:] = x
        
        if x.ndim == 2:
            x = x.unsqueeze(0)
        
        with torch.no_grad():
            for idx_srt in range(0, self.seq_len + target_len, self.pred_len):
                
                if self.seq_len + target_len - idx_srt < self.pred_len:
                    idx_end = -1
                    idx_srt = idx_end - self.seq_len
                else:
                    idx_end = idx_srt + self.pred_len
                    
                x_target = torch.zeros_like(x).to(x.device)
                
                pred = self.forward(x, x_target).squeeze(0)
                output[idx_srt : idx_end,:] = pred
                
        output = output[self.seq_len:, :].unsqueeze(0)
        
        return output

